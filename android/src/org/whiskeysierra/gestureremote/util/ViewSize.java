package org.whiskeysierra.gestureremote.util;

public final class ViewSize {

    private final int width;
    private final int height;

    public ViewSize(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

}
